package ar.alme.utils;

public class URLShorteningServiceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public URLShorteningServiceException() {
	}

	public URLShorteningServiceException(String arg0) {
		super(arg0);
	}

	public URLShorteningServiceException(Throwable arg0) {
		super(arg0);
	}

	public URLShorteningServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
