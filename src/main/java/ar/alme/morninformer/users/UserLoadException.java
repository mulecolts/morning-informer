package ar.alme.morninformer.users;

public class UserLoadException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public UserLoadException() {
	}

	public UserLoadException(String arg0) {
		super(arg0);
	}

	public UserLoadException(Throwable arg0) {
		super(arg0);
	}

	public UserLoadException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
