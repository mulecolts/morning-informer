package ar.alme.morninformer;

public class ChannelOpeningException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ChannelOpeningException() {
	}

	public ChannelOpeningException(String arg0) {
		super(arg0);
	}

	public ChannelOpeningException(Throwable arg0) {
		super(arg0);
	}

	public ChannelOpeningException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
